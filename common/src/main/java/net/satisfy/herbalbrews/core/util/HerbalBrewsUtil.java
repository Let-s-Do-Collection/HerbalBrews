    public static boolean matchesRecipe(RecipeInput recipeInput, NonNullList<Ingredient> recipe, int startIndex, int endIndex) {
        List<ItemStack> validStacks = new ArrayList<>();

        for(int i = startIndex; i <= endIndex; ++i) {
            ItemStack stackInSlot = recipeInput.getItem(i);
            if (!stackInSlot.isEmpty()) {
                validStacks.add(stackInSlot);
            }
        }

        Iterator<Ingredient> var10 = recipe.iterator();

        boolean matches;
        do {
            if (!var10.hasNext()) {
                return true;
            }

            Ingredient entry = var10.next();
            matches = false;

            for (ItemStack item : validStacks) {
                if (entry.test(item)) {
                    matches = true;
                    validStacks.remove(item);
                    break;
                }
            }
        } while(matches);

        return false;
    }

    public static boolean hasFrostWalker(LivingEntity entity) {
        return entity.getItemBySlot(EquipmentSlot.FEET).get(DataComponents.ENCHANTMENTS).equals(Enchantments.FROST_WALKER);
    }
